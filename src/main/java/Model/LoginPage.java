package Model;

public class LoginPage {
  private String u_name;
  private String pass;
public LoginPage() {
	super();
	this.u_name = u_name;
	this.pass = pass;
}
public String getU_name() {
	return u_name;
}
public void setU_name(String u_name) {
	this.u_name = u_name;
}
public String getPass() {
	return pass;
}
public void setPass(String pass) {
	this.pass = pass;
}
  
}
